export const getDictionary = (locale: string) => {
  // Simple mapping: if locale starts with 'tr', use Turkish. Otherwise English.
  // This handles 'tr', 'tr-TR' etc.
  if (locale && locale.toLowerCase().startsWith("tr")) {
    return dictionary.tr
  }
  return dictionary.en
}
